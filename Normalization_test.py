#正規化するだけのプログラム
#値を０−１に正規化
def min_max(x):
    xmin = min(x)
    xmax = max(x)
    result = []

    for i in x:
        result.append((i-xmin)/(xmax-xmin))

    return result





a = [0.36082067713534877, 0.4469373806490287, 0.6071084130739376, 0.25744213039521674, 0.387819420167498, 0.3046010465884111, 0.0, 0.5207407569067793, 0.435788775971319, 1.0, 0.5009528371347226, 0.34181889565803253]
print("正規化前 : " + str(a))

b = min_max(a)
print("正規化後 : " + str(b))
